<script>
export default {
  props: {
    matched: {
      type: Boolean,
      default: false,
    },
    position: {
      type: Number,
      required: true,
    },
    value: {
      type: String,
      required: true,
    },
    visible: {
      type: Boolean,
      default: false,
    },
  },
  setup(props, context) {
    const selectCard = () => {
      context.emit('select-card', {
        position: props.position,
        faceValue: props.value,
      });
    };

    return {
      selectCard,
    };
  },
};
</script>

<template>
  <div class="card" @click="selectCard">
    <div v-if="visible" class="card-face is-front">
      {{ value }}
      <div v-if="matched" class="correct">correct!</div>
    </div>
    <div v-else class="card-face is-back">Back</div>
  </div>
</template>

<style>
.card {
  position: relative;
}

.card-face {
  width: 100%;
  height: 100%;
  position: absolute;
  border-radius: 7px;
  cursor: pointer;
}

.card-face.is-front {
  background-color: purple;
  color: white;
  font-size: 20px;
}

.card-face.is-back {
  background-color: orange;
  color: white;
}

.correct {
  position: absolute;
  bottom: 5px;
  right: 5px;
  color: lightgreen;
}
</style>
